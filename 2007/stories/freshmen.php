<h2>Freshmen on the Team</h2>
<br />
<p>Every year, the team launches and extensive recruitment program in Staten Island Technical High School.  Starting the very first week of school, we fill the hallways with posters and the airways with announcements telling of the first FIRST robotics meeting in school.  We look forward to recieving the new freshman.  We offer tours of the shop and display the robots for them.  This year, we asked some of the freshmen about how they feel about the team and robotics.  Here's what they had to say.</p>
<br />
<p>I was a little reluctant to join the robotics team at first, because I'm not the most technical person in the world.  However, the animation took me in, and showed me a lot of cool and really interesting things.  I learned about computers, how to use 3ds Max, Photoshop, and lots of other software.  Mike and Daniil have been really great about showing me the ropes and getting me settled into robotics.  Learning animation has even helped me with school.  I find myself frequently using it in presentations and projects.<br />
<i>- Vivek Laungani</i></p>
<br />
<p>When you first try out for the robotics team in the begining of the school year, you are wondering what the team is really like. You meet new students, most of them being upper classmen, and have to get used to the policies of the team. As time progresses, you learn more about the team and it's members, making new friends who all have interests close to yours. You expect the upper classmen to be demanding and expect them to order you around; but, to your surprise, most of them don't. You are accepted to the team by showing dedication and smarts, even if you aren't the strongest and most athletic. The team is a place where you can hang out after school and learn interesting things that can help you in the near and far future, while letting you learn of your true skills that you never knew you had.<br />
<i>- Graham (Cookie) Freedland</i></p>
<br />
<p>Both freshmen are having a wonderful time and are anxious to see what the competitions are like.  They are look forward to rest of the season and want to remain in robotics for the remainder of their high school careers.</p>


