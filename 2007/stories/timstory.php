<h2>Words of a FIRST Alumni – Interview with Timothy Fedullo</h2>
<h1>Michael Iannelli</h1>
<br />
 
<p><img alt="" src="stories/t1.jpg" class="marbor" align="left" />Timothy Fedullo graduated last June, but never truly forgot his love and obsession with robotics.  As the captain of school’s robotics team for two years, he spent time working on a variety of different aspects of the robot and competition.  He began each year by mentoring the FLL team of his former school, “”.  He continued his volunteering in FLL by refereeing at many of the New York FLL events.  When the build season would come each year, it was Tim’s true time to shine.  Working on aspects of electronics, programming, and mechanical, he was the jack of all trades.  When the competitions came, he displayed immense skill as a driver and strategist for three years.  Tim is currently attending The Cooper Union in Manhattan, New York.  He frequently comes back to school to visit the robotics lab and offer helpful advice and knowledge.</p>
<br />
<p><img alt="" src="stories/t2.jpg" class="marbor" align="right" />
<strong>MI</strong>: How is school?<br />
<strong>TF</strong>: School is fun.  All my classes are interesting.  I am even enjoying humanities.<br />
<strong>MI</strong>: Do you miss Tech?<br />
<strong>TF</strong>: Of course.  Tech was awesome.<br />
<strong>MI</strong>: What about robotics?<br />
<strong>TF</strong>: No not at all.  I’m just kidding.  Of course I miss robotics.<br />
<strong>MI</strong>: What do you miss most about it?<br />
<strong>TF</strong>: Everything.  The team, Building the robot, and the competitions.  I still replay matches in my head.<br />
<strong>MI</strong>: Which matches in particular?<br />
<strong>TF</strong>: O lot’s of them.  There’s the time I drove against 25 in New Jersey and the fade away – on the buzzer shot in one of the Jersey Elimination Matches.<br />
<strong>MI</strong>: You seem to like the matches in Jersey.<br />
<strong>TF</strong>: Yes.  They were much more challenging and fun than the others we competed in.<br />
<strong>MI</strong>: How has robotics prepared you for college?<br />
<strong>TF</strong>: Robotics involved lots of skills that I am using now.  The clearest example though is the where we are taking apart a robot arm.  I am the only person that knows what’s going on.  Other than the technical background that FIRST Robotics has given me, it has helped me to work in teams and given me excellent leadership skills.<br />
<strong>MI</strong>: What are you majoring in?<br />
<strong>TF</strong>: Mechanical Engineering.  So far it’s tons of fun.  Everyone takes the same classes in freshman year, so I really don’t know much about the mechanical engineering specific classes.<br />
<strong>MI</strong>: What are your plans for the future?<br />
<strong>TF</strong>: I hope to get a job robotics related.  NASA Rovers would be awesome.<br />
<strong>MI</strong>: What are your favorite memories from robotics?<br />
<strong>TF</strong>: I have so many of those.  In 2003 when we got a score of 273.  That was one of the highest scores reported in the country.  I remember our robot from 2004 and the disaster with the arm breaking right before ship.  The final New Jersey matches in 2005 when we upset the defending champions and favored team to win.  And all of last year, from the amazing Trenton matches to when we watched NSF2-2.<br />
<strong>MI</strong>: Wow!  It really is amazing how much you remember.<br />
<strong>TF</strong>: I loved robotics.  You guys were like a second family to me.  We were there for each other in the tough times of build season and there to celebrate with each other after the regionals.  You can count on me to watch the webcasts and support you this year.  I can’t wait for the competitions to begin.<br />
</p>

