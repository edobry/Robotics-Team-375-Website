<h2>Links</h2>
<p>
<h1>General Links</h1>
<strong>Official FIRST Website</strong><br />
<a href="http://www.usfirst.org">http://www.usfirst.org</a><br />
The Official FIRST Website is the primary resource for all FIRST Teams.  It contains information for current Lego, Vex, and FRC Teams as well as information for starting your own team.
<br />
<br />
<strong>FIRST Forums</strong><br />
<a href="http://forums.usfirst.org/">http://forums.usfirst.org/</a><br />
The Official FIRST Forum is an excellent source of the latest announcements.
<br />
<br />
<strong>FIRST Wiki</strong><br />
<a href="http://www.firstwiki.org">http://www.firstwiki.org</a><br />
FIRST Wiki is an online, editable encyclopedia that tells the history of FIRST and its founders, as well as manuals and archived information of teams, robots, and past competitions.  We encourage all teams to edit FIRST Wiki and add their team's own page to the encyclopedia.
<br />
<br />
<strong>FIRSTr</strong><br />
<a href="http://www.FIRSTr.org">http://www.FIRSTr.org</a><br />
FIRSTr is a Web 2.0 style website that presents the coolest and latest articles in FIRST by usung a system where members decide on the popularity and quality of each article.
<br />
<br />
<strong>Chief Delphi</strong><br />
<a href="http://www.chiefdelphi.com">http://www.chiefdelphi.com</a><br />
The Chief Delphi Forum is the major hangout for all FIRST teams, where members discuss robotics, strategy, or general information.
<br />
<br />
<strong>NEMO</strong><br />
<a href="http://www.firstnemo.org/">http://www.firstnemo.org/</a><br />
The Non Engineering Mentor Organization aka NEMO is open to any adult mentor involved partially or fully on the non engineering part of the team.
</p>
