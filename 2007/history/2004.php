<h2>Season 2004</h2>

<img alt="" src="media/pictures/2004/ny/1.jpg" height="213px" width="320px" align="left" class="marbor" />
<p>The 2004 season was an unusual year for team 375.  Our head mentor, Mr. Siegel, was injured during a robotics meeting.  The accident unfortunately caused him to miss much of the build season.  Without Mr. Siegel, the robot progresses slowly but nevertheless progressed.  Another problem soon occurred when the robot's arm broke right before ship date and we were forced to replace the arm with a grappling hook.  Despite numerous setbacks, the team did exceptionally well at the tournaments, seeding in the Trenton Regional. and reaching the semifinals in both the the Trenton and New Jersey Regionals.</p><br clear="all" />
<p><strong>The Robot:</strong> Robotic Reaper<br />
The 2004 robot was designed for picking up large balls and herding smaller balls.  When the arm broke, it was removed and replaced with grappling hook that would allow to robot to hang on the bar and collect bonus points.</p>
