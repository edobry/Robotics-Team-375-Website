<p><strong>The Robot:</strong> The Tank<br />
The Tank was 375's robot in the 2003 FIRST Robotics Competition.  It was desinged to be tough and dyrable and play a strong defensive game.  It had 2 piston driven arms that would extend, increasing the amount of boxes the robot could push.  The robot had an ingenius bonus point gathering system which allowed it to use suction cups to stick to the low friction bonus platform and remain unfazed by robots who threatened to push it away.</p>
