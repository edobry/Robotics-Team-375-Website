<h2>Photoshop Tutorial- Text with Aura</h1>
<h1>by Vivek Laungani</h1>
<br />
<p>Creating a text with aura is a very cool and easy thing to do. The aura on the text stands out and is very attractive. These easy steps are mentioned below:</p>
<br />
<ol>
<li>Make the foreground color white and the background color black. (If your foreground and background colors are black and white, respectively, just press X to inverse the colors).</li>
<li>Create a new Photoshop Document 1117 x 790. Make the options as shown below:</li>
<img alt="" src="tutorials/au/01.jpg" class="marbor" />
<li>Click on the Type Tool (T) and type your text in fairly large character size (approx. 225).</li>
<img alt="" src="tutorials/au/02.jpg" class="marbor" />
<li>Click Filter>Distort>Polar Coordinates. It will ask you to rasterize the layer. Just click OK. When you get to the Polar Coordinates box, click on Polar to Rectangular.</li>
<img alt="" src="tutorials/au/03.jpg" class="marbor" />
<li>Click Filter>Stylize>Wind. Click OK once the Wind box comes up. Click CTRL + F do apply the filter once more. Now go back to the Wind box (Filter>Stylize>Wind). Now look for Direction. Change the Direction from “From the Right” to “From the Left”. Now click OK. Apply the filter once more (CTRL + F). Your text should look somewhat like this:</li>
<img alt="" src="tutorials/au/04.jpg" class="marbor" />
<li>Now click Image>Rotate Canvas>90&deg; CW. Click on Filter>Stylize>Wind and make the Direction “From the Right”. Click OK. Now apply the Filter again (CTRL + F). Rotate the canvas back again by clicking Image>Rotate Canvas>90o CCW. It should look like this:</li>
<li>Now click Filter>Distort>Polar Coordinates. Change the settings from “Polar to Rectangular” to “Rectangular to Polar”.</li>
<li>Make a new layer. Fill it with a color of your choice. On the Layers Panel, right-click on the tab and click Blending Options. In scroll down on the Blend Mode menu and click Color Burn. Now press OK. Presto! There you go!</li>
</ol>
