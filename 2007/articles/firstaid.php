<h2>Safety Practices</h2>
<h1>by Team 375</h1><br />

<p><strong>First Aid Kits</strong><br />
Every basic First Aid Kit should include the following in order to sufficiently be prepared for emergencies:
<ul>
<li>Bandages</li>
<li>Sterile Gauze Pads</li>
<li>Antiseptic Spray</li>
<li>Adhesive Tape</li>
<li>Latex Gloves</li>
<li>Scissors</li>
<li>Cold Compress</li>
<li>Eye Protection</li>
<li>Safety Pin</li>
<li>Tweezers</li>
</ul></p>
<br />
<p><strong>Safety Glasses</strong><br />
Safety Goggles are required at all times in the pits.  They are designed to keep your eyes safe from dangerous air-born particles.</p>
<br />
<p><strong>Safety Gloves</strong><br />
Safety Gloves are designed to keep your hands safe from moving parts and electric shock.  They should be worn when cutting or drilling, when handling sharp materials, and when lifting the robot.</p>
<br />
<p><strong>Cuts</strong><br />
In the case of a cut, rinse wound thoroughly.  Apply pressure to the cut with sterile gauze, a bandage, or a clean cloth.  Apply more bandages or cloths if necessary and continue applying pressure.  Elevate the wound to slow down the bleeding.  Once bleeding has stopped, cover the wound in another clean bandage.</p>
<br />
<p><strong>Burns</strong><br />
Remove clothing from the burned areas, except for clothing stuck to the skin. Run cool, not cold, water over the burn until the pain subsides. Lightly apply a gauze bandage if the injury is a small, first-degree burn.</p>
<br />
<p><strong>Broken Bones</strong><br />
Remove clothing from injured body part.  Apply a cold compress or ice pack wrapped in cloth to the wound.  Place a splint on the injured  body part by: 
<ul>
<li>Keeping the injured limb in the position.<li>
<li>Placing soft padding around the injured part<li>
<li>Placing something firm next to the injured part.  It must be long enough to go past the joint and below the injury.<li>
<li>Keep the splint in place with tape.<li>
<li>Seek the assistance of a medical personnel, and do not eat, in case surgery is needed.</li>
</ul></p>
<br />
<p><strong>Serious Accidents</strong><br />
If any serious accident occurs, immediately seek the assistance of trained Medical Personnel in the area.</p>





