<h2>What We Do</h2>
<p>Team 375 isn't just a quiet team that keeps to themselves in their lab. Far from it. The Robotic Plague mentors various teams and helps host various tournaments around the New York City area. We have a vast network of students working in specific groups to get the job done; we don't waste a minute.</p>
<p>Team 375 founded the FIRST Lego League Staten Island Tournament.  It was the first borough tournament to be held in New York aside from official New York City Tournament and set the precident for the other Borough Lego Tournaments.  We hold the Lego Tournament every year in November.</p>
<p>Team 375 has worked to spread the messages and ideas of FIRST to others through a demonstration program.  At school open houses and information nights we set up display with robotis from previous years performing their intended purposes.  Aside from showing off the robot, the demonstrations are meant to inform others about our FIRST Robotics Team and the FIRST organization.  In 2006, our team was invited to demonstrate our robot and lego robotics program at Sony Wonder Labs and NextFest, a premiere technology expo.</p>

